<?php

namespace LaraPress\Pipeline;

use LaraPress\Contracts\Pipeline\Hub as PipelineHubContract;
use LaraPress\Contracts\Support\DeferrableProvider;
use LaraPress\Support\ServiceProvider;

class PipelineServiceProvider extends ServiceProvider implements DeferrableProvider
{
    /**
     * Register the service provider.
     *
     * @return void
     */
    public function register()
    {
        $this->app->singleton(
            PipelineHubContract::class, Hub::class
        );
    }

    /**
     * Get the services provided by the provider.
     *
     * @return array
     */
    public function provides()
    {
        return [
            PipelineHubContract::class,
        ];
    }
}
